package leetCode.medium

import java.util.TreeSet
import kotlin.math.abs
import kotlin.math.max

class MinimumAbsoluteSumDifference {
    //1 найти место, которое даёт максимальную разницу
    //2 далее сортируем nums1
    //3 находим ближайшее число из пункта 1
    fun minAbsoluteSumDiff(nums1: IntArray, nums2: IntArray): Int {
        var sum = 0L
        val module = 1000000007

        val set = TreeSet<Int>()

        for (num in nums1) {
            set.add(num)
        }

        var maxSaving = 0L
        for (i in nums1.indices) {
            val diff = abs(nums2[i] - nums1[i])
            sum += diff.rem(module)

            val floor = set.floor(nums2[i])?.toLong()
            val ceiling = set.ceiling(nums2[i])?.toLong()

            if (ceiling != null) {
                maxSaving = maxOf(maxSaving, diff - abs(ceiling - nums2[i]))
            }
            if (floor != null) {
                maxSaving = maxOf(maxSaving, diff - abs(floor - nums2[i]))
            }
        }

        return (sum - maxSaving).rem(module).toInt()
    }
}

